"""One-time repair: re-upsert every daily quiz to Supabase with correctly
aligned question/answer pairs.

Background: the old extraction used two independent regex scans for EN and
Answer lines. When the LLM wrote extra '- Answer:' lines inside Steps (or a
question had no Answer line), the answers array shifted — students were graded
against the wrong answers and Mistake History showed mismatched pairs.

Usage (PowerShell):
  python scripts/repair_quizzes.py          # dry run — shows what would change
  python scripts/repair_quizzes.py --sql    # writes repair_quizzes.sql for the Supabase SQL editor
  python scripts/repair_quizzes.py --apply  # re-upserts via the API (needs env vars below)

For --apply:
  $env:PUBLIC_API_BASE = "https://dailymathforkids-api.vercel.app"
  $env:SUPABASE_SERVICE_ROLE = "<service role key>"
"""
import json
import pathlib
import re
import sys

from gen_site import parse_grade_sections, extract_qa_pairs, upsert_quiz_to_supabase

ROOT = pathlib.Path(__file__).resolve().parents[1]
DAILY_DIR = ROOT / "daily"


def sql_str(value):
    """Escape a string for a SQL single-quoted literal."""
    return "'" + str(value).replace("'", "''") + "'"


def main(apply=False, sql=False):
    total, fixed = 0, 0
    sql_lines = [
        "-- Repair misaligned quiz question/answer arrays.",
        "-- Generated by scripts/repair_quizzes.py — paste into the Supabase SQL editor and Run.",
        "",
    ]
    broken_ids = []
    for md_path in sorted(DAILY_DIR.glob("*.md")):
        day = md_path.stem
        text = md_path.read_text(encoding="utf-8")
        for code, content in parse_grade_sections(text).items():
            if not content.strip():
                continue
            total += 1
            old_qs = re.findall(r'^\s*-\s*EN:\s*(.+)', content, re.MULTILINE)
            old_ans = re.findall(r'^\s*-\s*Answer:\s*(.+?)(?:\s*$)', content, re.MULTILINE)
            qs, ans = extract_qa_pairs(content)
            was_broken = (len(old_qs) != len(old_ans)) or (old_ans != ans)
            if not was_broken:
                continue
            fixed += 1
            quiz_id = f"{day}-{code}"
            broken_ids.append(quiz_id)
            print(f"{'FIXING' if apply else 'WOULD FIX'}: {quiz_id} "
                  f"(old: {len(old_qs)}q/{len(old_ans)}a -> new: {len(qs)}q/{len(ans)}a)")
            if apply:
                upsert_quiz_to_supabase(quiz_id, qs, ans)
            if sql:
                q_json = sql_str(json.dumps(qs, ensure_ascii=False))
                a_json = sql_str(json.dumps(ans, ensure_ascii=False))
                sql_lines.append(
                    f"insert into quizzes (id, questions, answers)\n"
                    f"values ({sql_str(quiz_id)}, {q_json}::jsonb, {a_json}::jsonb)\n"
                    f"on conflict (id) do update set questions = excluded.questions, answers = excluded.answers;"
                )
    print(f"\nDone. {fixed} of {total} quiz records {'re-upserted' if apply else 'need fixing'}.")
    if sql:
        sql_lines.append("")
        sql_lines.append("-- OPTIONAL: remove quiz mistakes recorded from these corrupted quizzes.")
        sql_lines.append("-- These rows may pair questions with the wrong answers, so they are unreliable.")
        ids = ", ".join(sql_str(i) for i in broken_ids)
        sql_lines.append(f"-- delete from mistakes where source = 'quiz' and quiz_id in ({ids});")
        out = ROOT / "repair_quizzes.sql"
        out.write_text("\n".join(sql_lines), encoding="utf-8")
        print(f"SQL written to {out} — paste its contents into the Supabase SQL editor.")
    elif not apply and fixed:
        print("Run again with --sql to generate a SQL file, or --apply to push via the API.")


if __name__ == "__main__":
    main(apply="--apply" in sys.argv, sql="--sql" in sys.argv)
